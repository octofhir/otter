//! ECMA-262 §25.1 ArrayBuffer bootstrap installer.
//!
//! Replaces the placeholder with a real callable + constructible
//! `NativeFunction`. Statics: `isView`. Prototype: `slice` +
//! `resize` + `transfer` + `transferToFixedLength`, plus accessor
//! getters for `byteLength`, `maxByteLength`, `resizable`,
//! `detached`.
//!
//! Prototype methods delegate to
//! [`crate::binary::array_buffer_prototype`] native helpers.
//!
//! # See also
//! - <https://tc39.es/ecma262/#sec-arraybuffer-constructor>
//! - <https://tc39.es/ecma262/#sec-properties-of-the-arraybuffer-prototype-object>

use otter_bytecode::method_id::{ArrayBufferMethod, SharedArrayBufferMethod};

use crate::binary::dispatch;
use crate::js_surface::JsSurfaceError;
use crate::object::{self, JsObject, PartialPropertyDescriptor};
use crate::{NativeCtx, NativeError, Value, VmError};

// `ARRAYBUFFER_SPEC` + `ArrayBufferIntrinsic` generated by `couch!`.
// §25.1 — prototype chains to %Object.prototype% by couch! default;
// constructor back-pointer auto-installed.
otter_macros::couch! {
    name = "ArrayBuffer",
    feature = CORE,
    intrinsic = ArrayBufferIntrinsic,
    constructor = (length = 1, call = ab_ctor_call),
    statics = {
        "isView" / 1 => ab_is_view,
    },
    prototype = {
        methods = {
            "slice"                 / 2 => ab_slice,
            "resize"                / 1 => ab_resize,
            "transfer"              / 1 => ab_transfer,
            "transferToFixedLength" / 1 => ab_transfer_to_fixed_length,
        },
        accessors = [
            ("byteLength",    get = ab_byte_length),
            ("maxByteLength", get = ab_max_byte_length),
            ("resizable",     get = ab_resizable),
            ("detached",      get = ab_detached),
        ],
    },
}

// `SHAREDARRAYBUFFER_SPEC` + `SharedArrayBufferIntrinsic` generated
// by `couch!`. §25.2 — shares the `JsArrayBuffer` substrate with
// `ArrayBuffer`; `is_shared` distinguishes the two value-side.
// `transfer` / `transferToFixedLength` belong to ArrayBuffer only.
otter_macros::couch! {
    name = "SharedArrayBuffer",
    feature = CORE,
    intrinsic = SharedArrayBufferIntrinsic,
    constructor = (length = 1, call = sab_ctor_call),
    prototype = {
        methods = {
            "slice" / 2 => ab_slice,
            "grow"  / 1 => sab_grow,
        },
        accessors = [
            ("byteLength",    get = ab_byte_length),
            ("maxByteLength", get = ab_max_byte_length),
            ("growable",      get = sab_growable),
        ],
    },
}

fn sab_ctor_call(ctx: &mut NativeCtx<'_>, args: &[Value]) -> Result<Value, NativeError> {
    if !ctx.is_construct_call() {
        return Err(NativeError::TypeError {
            name: "SharedArrayBuffer",
            reason: "constructor requires 'new'".to_string(),
        });
    }
    let roots = ctx.collect_native_roots();
    let this_value = *ctx.this_value();
    let new_target = ctx.new_target().cloned();
    let mut external_visit = |visitor: &mut dyn FnMut(*mut otter_gc::raw::RawGc)| {
        crate::runtime_cx::visit_native_roots(
            visitor,
            &roots,
            &this_value,
            new_target.as_ref(),
            &[],
            &[args],
        );
    };
    let value = dispatch::shared_array_buffer_call_with_roots(
        SharedArrayBufferMethod::Construct,
        args,
        ctx.heap_mut(),
        &mut external_visit,
    )
    .map_err(|e| vm_to_native(e, "SharedArrayBuffer"))?;
    // §10.1.13 GetPrototypeFromConstructor — derived `super()`
    // construction forwards `new.target`, so the allocated exotic
    // receives `Subclass.prototype` as its observable [[Prototype]].
    // <https://tc39.es/ecma262/#sec-getprototypefromconstructor>
    let needs_proto_override = ctx.new_target().is_none_or(|v| !v.is_native_function());
    if needs_proto_override
        && let Some(proto) =
            crate::bootstrap::native_new_target_prototype(ctx, "SharedArrayBuffer")?
    {
        ctx.interp_mut()
            .set_non_gc_exotic_prototype_override(&value, Some(proto));
    }
    Ok(value)
}

/// §25.2.5.4 — `SharedArrayBuffer.prototype.grow(newByteLength)`.
fn sab_grow(ctx: &mut NativeCtx<'_>, args: &[Value]) -> Result<Value, NativeError> {
    const NAME: &str = "SharedArrayBuffer.prototype.grow";
    let buf = receiver_ab(ctx, NAME)?;
    if !buf.is_shared() {
        return Err(NativeError::TypeError {
            name: NAME,
            reason: "receiver is not a growable shared arraybuffer".to_string(),
        });
    }
    let new_len = match crate::binary::to_index(
        args.first().unwrap_or(&Value::undefined()),
        ctx.heap(),
    ) {
        Some(n) => n as usize,
        None => {
            return Err(NativeError::TypeError {
                name: NAME,
                reason: "newByteLength must be a non-negative integer".to_string(),
            });
        }
    };
    if !buf.grow(ctx.heap_mut(), new_len) {
        return Err(NativeError::TypeError {
            name: NAME,
            reason: "cannot grow".to_string(),
        });
    }
    Ok(Value::undefined())
}

fn sab_growable(ctx: &mut NativeCtx<'_>, _args: &[Value]) -> Result<Value, NativeError> {
    let b = receiver_ab(ctx, "get SharedArrayBuffer.prototype.growable")?;
    Ok(Value::boolean(b.is_shared() && b.is_growable(ctx.heap())))
}

/// Install `SharedArrayBuffer.prototype[@@toStringTag] = "SharedArrayBuffer"`.
pub fn install_shared_array_buffer_well_knowns_post_bootstrap(
    heap: &mut otter_gc::GcHeap,
    global: JsObject,
    well_known: &crate::symbol::WellKnownSymbols,
) -> Result<(), JsSurfaceError> {
    use crate::symbol::WellKnown;

    let Some(ctor) =
        object::get(global, heap, "SharedArrayBuffer").and_then(|v| v.as_native_function())
    else {
        return Ok(());
    };
    let descriptor = ctor
        .own_property_descriptor(heap, "prototype")
        .map_err(|_| JsSurfaceError::OutOfMemory)?;
    let prototype = match descriptor.and_then(|d| match d.kind {
        crate::object::DescriptorKind::Data { value } => value.as_object(),
        _ => None,
    }) {
        Some(p) => p,
        None => return Ok(()),
    };
    let tag = crate::string::JsString::from_str("SharedArrayBuffer", heap)
        .map_err(|_| JsSurfaceError::OutOfMemory)?;
    object::define_own_symbol_property_partial(
        prototype,
        heap,
        well_known.get(WellKnown::ToStringTag),
        PartialPropertyDescriptor {
            value: Some(Value::string(tag)),
            writable: Some(false),
            enumerable: Some(false),
            configurable: Some(true),
            ..Default::default()
        },
    );
    Ok(())
}

