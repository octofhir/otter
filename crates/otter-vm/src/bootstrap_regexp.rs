//! ECMA-262 §22.2 RegExp bootstrap installer.
//!
//! Installs the JS-visible `RegExp` constructor + prototype via
//! `couch!`. The prototype carries `exec` / `test` / `toString` /
//! `compile` as own data properties and the spec-mandated accessor
//! getters for `source` / `flags` / `global` / `ignoreCase` /
//! `multiline` / `dotAll` / `unicode` / `sticky` / `hasIndices` /
//! `unicodeSets`. `@@toStringTag` + symbol-keyed protocol methods
//! land in [`install_regexp_well_knowns_post_bootstrap`]. The B.2.4
//! legacy static accessors (`RegExp.input`, `$1`..`$9`, …) run via
//! the `post_install` hook because they capture the ctor identity.
//!
//! # Contents
//! - `Intrinsic` — generated by `couch!`.
//! - [`install_regexp_well_knowns_post_bootstrap`] — symbol fixup.
//!
//! # Invariants
//! - `new RegExp(pattern, flags)` and bare `RegExp(...)` both
//!   produce a fresh RegExp value. Per §22.2.3.1 step 1, when
//!   `pattern` is itself a RegExp and `flags` is `undefined` and
//!   the new-target is the active `RegExp` constructor, the
//!   incoming RegExp is returned unchanged.
//! - Prototype accessor getters throw `TypeError` when `this` is
//!   not a RegExp (exception: `RegExp.prototype` itself returns
//!   `"(?:)"` for `source` and `""` for `flags`).
//! - Legacy static accessor constructors, captures, getters, and setters stay
//!   in one canonical root scope across every paired native allocation.
//!
//! # See also
//! - <https://tc39.es/ecma262/#sec-regexp-constructor>
//! - <https://tc39.es/ecma262/#sec-properties-of-the-regexp-prototype-object>

use crate::js_surface::JsSurfaceError;
use crate::object::{self, JsObject, PropertyDescriptor};
use crate::regexp::{JsRegExp, RegExpFlags};
use crate::rooting::RootScopeExt;
use crate::string::JsString;
use crate::{NativeCtx, NativeError, Value};

otter_macros::couch! {
    name = "RegExp",
    feature = CORE,
    constructor = (length = 2, call = regexp_ctor_call),
    statics = {
        "escape" / 1 => regexp_escape,
    },
    prototype = {
        methods = {
            "exec"     / 1 => proto_exec,
            "test"     / 1 => proto_test,
            "toString" / 0 => proto_to_string,
            "compile"  / 2 => proto_compile,
        },
        accessors = [
            ("source",      get = accessor_source),
            ("flags",       get = accessor_flags),
            ("global",      get = accessor_global),
            ("ignoreCase",  get = accessor_ignore_case),
            ("multiline",   get = accessor_multiline),
            ("dotAll",      get = accessor_dot_all),
            ("unicode",     get = accessor_unicode),
            ("sticky",      get = accessor_sticky),
            ("hasIndices",  get = accessor_has_indices),
            ("unicodeSets", get = accessor_unicode_sets),
        ],
    },
    post_install = install_regexp_legacy_accessors,
}

/// §B.2.4 RegExp legacy static accessors — `RegExp.input`, `$_`,
/// `lastMatch`, `$&`, `lastParen`, `$+`, `leftContext`, `` $` ``,
/// `rightContext`, `$'`, plus `$1`..`$9`. Each is an accessor
/// pair installed on the `%RegExp%` constructor with `[[Enumerable]]
/// = false`, `[[Configurable]] = true`. The getters / setters
/// validate that `this` is `%RegExp%` per `GetLegacyRegExpStaticProperty`
/// / `SetLegacyRegExpStaticProperty` and otherwise return empty
/// strings (we don't yet track per-realm last-match state).
///
/// # See also
/// - <https://tc39.es/ecma262/#sec-additional-properties-of-the-regexp.prototype-object>
fn install_regexp_legacy_accessors(
    heap: &mut otter_gc::GcHeap,
    _global: JsObject,
    ctor: crate::native_function::NativeFunction,
) -> Result<(), JsSurfaceError> {
    use crate::native_function::NativeFunction;

    fn install_get_only(
        heap: &mut otter_gc::GcHeap,
        ctor: Value,
        name: &'static str,
        getter_name: &'static str,
    ) -> Result<(), JsSurfaceError> {
        let mut ctor_root = ctor;
        let mut getter_root = Value::undefined();
        let mut roots = otter_gc::RootScope::new(heap);
        // SAFETY: both canonical slots precede the scope and are reloaded
        // after the getter allocation before descriptor installation.
        unsafe {
            roots.add_value(&mut ctor_root);
            roots.add_value(&mut getter_root);
        }
        let captures: smallvec::SmallVec<[Value; 4]> = smallvec::smallvec![ctor_root];
        getter_root = Value::native_function(
            NativeFunction::with_length_and_captures(
                heap,
                getter_name,
                0,
                legacy_accessor_getter,
                captures,
            )
            .map_err(|_| JsSurfaceError::OutOfMemory)?,
        );
        let ctor = ctor_root
            .as_native_function()
            .expect("RegExp constructor remains rooted");
        let desc = PropertyDescriptor::accessor(Some(getter_root), None, false, true);
        if !ctor.define_own_property(heap, name, desc) {
            return Err(JsSurfaceError::DefinePropertyFailed(name));
        }
        Ok(())
    }

    fn install_get_set(
        heap: &mut otter_gc::GcHeap,
        ctor: Value,
        name: &'static str,
        getter_name: &'static str,
        setter_name: &'static str,
    ) -> Result<(), JsSurfaceError> {
        let mut ctor_root = ctor;
        let mut getter_root = Value::undefined();
        let mut setter_root = Value::undefined();
        let mut roots = otter_gc::RootScope::new(heap);
        // SAFETY: the canonical slots precede the scope and no references to
        // them are retained across the getter/setter allocation safepoints.
        unsafe {
            roots.add_value(&mut ctor_root);
            roots.add_value(&mut getter_root);
            roots.add_value(&mut setter_root);
        }
        let g_captures: smallvec::SmallVec<[Value; 4]> = smallvec::smallvec![ctor_root];
        getter_root = Value::native_function(
            NativeFunction::with_length_and_captures(
                heap,
                getter_name,
                0,
                legacy_accessor_getter,
                g_captures,
            )
            .map_err(|_| JsSurfaceError::OutOfMemory)?,
        );
        let s_captures: smallvec::SmallVec<[Value; 4]> = smallvec::smallvec![ctor_root];
        setter_root = Value::native_function(
            NativeFunction::with_length_and_captures(
                heap,
                setter_name,
                1,
                legacy_accessor_setter,
                s_captures,
            )
            .map_err(|_| JsSurfaceError::OutOfMemory)?,
        );
        let ctor = ctor_root
            .as_native_function()
            .expect("RegExp constructor remains rooted");
        let desc = PropertyDescriptor::accessor(Some(getter_root), Some(setter_root), false, true);
        if !ctor.define_own_property(heap, name, desc) {
            return Err(JsSurfaceError::DefinePropertyFailed(name));
        }
        Ok(())
    }

    let mut ctor_root = Value::native_function(ctor);
    let mut roots = otter_gc::RootScope::new(heap);
    // SAFETY: the constructor slot precedes the scope and stays live until all
    // legacy accessor pairs have been installed.
    unsafe { roots.add_value(&mut ctor_root) };
    install_get_set(heap, ctor_root, "input", "get input", "set input")?;
    install_get_set(heap, ctor_root, "$_", "get $_", "set $_")?;
    install_get_only(heap, ctor_root, "lastMatch", "get lastMatch")?;
    install_get_only(heap, ctor_root, "$&", "get $&")?;
    install_get_only(heap, ctor_root, "lastParen", "get lastParen")?;
    install_get_only(heap, ctor_root, "$+", "get $+")?;
    install_get_only(heap, ctor_root, "leftContext", "get leftContext")?;
    install_get_only(heap, ctor_root, "$`", "get $`")?;
    install_get_only(heap, ctor_root, "rightContext", "get rightContext")?;
    install_get_only(heap, ctor_root, "$'", "get $'")?;
    install_get_only(heap, ctor_root, "$1", "get $1")?;
    install_get_only(heap, ctor_root, "$2", "get $2")?;
    install_get_only(heap, ctor_root, "$3", "get $3")?;
    install_get_only(heap, ctor_root, "$4", "get $4")?;
    install_get_only(heap, ctor_root, "$5", "get $5")?;
    install_get_only(heap, ctor_root, "$6", "get $6")?;
    install_get_only(heap, ctor_root, "$7", "get $7")?;
    install_get_only(heap, ctor_root, "$8", "get $8")?;
    install_get_only(heap, ctor_root, "$9", "get $9")?;
    Ok(())
}

/// §B.2.4.1 `GetLegacyRegExpStaticProperty` — verify `this` is the
/// `%RegExp%` constructor, otherwise raise `TypeError`. We don't yet
/// maintain per-realm last-match state, so the getter returns the
/// empty string (spec-permitted "no prior match" sentinel).
fn legacy_accessor_getter(
    ctx: &mut NativeCtx<'_>,
    _args: &[Value],
    captures: &[Value],
) -> Result<Value, NativeError> {
    let this_value = *ctx.this_value();
    let ctor = captures.first().cloned().unwrap_or(Value::undefined());
    if !values_strict_equal(&this_value, &ctor) {
        return Err(NativeError::TypeError {
            name: "RegExp legacy accessor",
            reason: "Method called on incompatible receiver".to_string(),
        });
    }
    Ok(Value::string(
        JsString::from_str("", ctx.heap_mut()).map_err(|_| NativeError::TypeError {
            name: "RegExp legacy accessor",
            reason: "out of memory".to_string(),
        })?,
    ))
}

/// §B.2.4.2 `SetLegacyRegExpStaticProperty` — accepts the assignment
/// only when `this` is `%RegExp%`, otherwise raises `TypeError`. We
/// drop the value because per-realm last-match storage isn't yet
/// implemented; this satisfies the spec-mandated receiver check and
/// the prop-desc tests that observe the setter shape.
fn legacy_accessor_setter(
    ctx: &mut NativeCtx<'_>,
    _args: &[Value],
    captures: &[Value],
) -> Result<Value, NativeError> {
    let this_value = *ctx.this_value();
    let ctor = captures.first().cloned().unwrap_or(Value::undefined());
    if !values_strict_equal(&this_value, &ctor) {
        return Err(NativeError::TypeError {
            name: "RegExp legacy accessor",
            reason: "Method called on incompatible receiver".to_string(),
        });
    }
    Ok(Value::undefined())
}

fn values_strict_equal(a: &Value, b: &Value) -> bool {
    match (a.as_native_function(), b.as_native_function()) {
        (Some(x), Some(y)) => x.ptr_eq(&y),
        _ => false,
    }
}

/// Install `@@toStringTag` (no longer set on every RegExp — it
/// lives on the prototype) and the Symbol-keyed methods
/// (`@@match`, `@@replace`, `@@search`, `@@split`, `@@matchAll`).
pub fn install_regexp_well_knowns_post_bootstrap(
    heap: &mut otter_gc::GcHeap,
    global: JsObject,
    well_known: &crate::symbol::WellKnownSymbols,
) -> Result<(), JsSurfaceError> {
    use crate::symbol::WellKnown;

    let Some(ctor) = object::get(global, heap, "RegExp").and_then(|v| v.as_native_function())
    else {
        return Ok(());
    };
    let descriptor = ctor
        .own_property_descriptor(heap, "prototype")
        .map_err(|_| JsSurfaceError::OutOfMemory)?;
    let prototype = match descriptor.and_then(|d| match d.kind {
        crate::object::DescriptorKind::Data { value } => value.as_object(),
        _ => None,
    }) {
        Some(p) => p,
        None => return Ok(()),
    };
    // §22.2.6.{8,10,11} `RegExp.prototype[@@match]` /
    // `RegExp.prototype[@@search]` / `RegExp.prototype[@@replace]` —
    // install native functions so user calls like
    // `re[Symbol.match]("…")` resolve through the spec-mandated
    // algorithm.
    let mut prototype_root = Value::object(prototype);
    let mut match_root = Value::undefined();
    let mut search_root = Value::undefined();
    let mut replace_root = Value::undefined();
    let mut split_root = Value::undefined();
    let mut match_all_root = Value::undefined();
    let mut roots = otter_gc::RootScope::new(heap);
    // SAFETY: all canonical slots precede the scope and remain stationary until
    // every native is allocated and installed on the prototype.
    unsafe {
        roots.add_value(&mut prototype_root);
        roots.add_value(&mut match_root);
        roots.add_value(&mut search_root);
        roots.add_value(&mut replace_root);
        roots.add_value(&mut split_root);
        roots.add_value(&mut match_all_root);
    }
    let match_sym = well_known.get(WellKnown::Match);
    let search_sym = well_known.get(WellKnown::Search);
    let replace_sym = well_known.get(WellKnown::Replace);
    let split_sym = well_known.get(WellKnown::Split);
    let match_all_sym = well_known.get(WellKnown::MatchAll);
    match_root = Value::native_function(
        crate::bootstrap::native_static_with_value_roots(
            heap,
            "[Symbol.match]",
            1,
            crate::regexp_prototype::native_regexp_symbol_match,
            &[],
        )
        .map_err(|_| JsSurfaceError::OutOfMemory)?,
    );
    search_root = Value::native_function(
        crate::bootstrap::native_static_with_value_roots(
            heap,
            "[Symbol.search]",
            1,
            crate::regexp_prototype::native_regexp_symbol_search,
            &[],
        )
        .map_err(|_| JsSurfaceError::OutOfMemory)?,
    );
    replace_root = Value::native_function(
        crate::bootstrap::native_static_with_value_roots(
            heap,
            "[Symbol.replace]",
            2,
            crate::regexp_prototype::native_regexp_symbol_replace,
            &[],
        )
        .map_err(|_| JsSurfaceError::OutOfMemory)?,
    );
    split_root = Value::native_function(
        crate::bootstrap::native_static_with_value_roots(
            heap,
            "[Symbol.split]",
            2,
            crate::regexp_prototype::native_regexp_symbol_split,
            &[],
        )
        .map_err(|_| JsSurfaceError::OutOfMemory)?,
    );
    match_all_root = Value::native_function(
        crate::bootstrap::native_static_with_value_roots(
            heap,
            "[Symbol.matchAll]",
            1,
            crate::regexp_prototype::native_regexp_symbol_match_all,
            &[],
        )
        .map_err(|_| JsSurfaceError::OutOfMemory)?,
    );
    let prototype = prototype_root
        .as_object()
        .expect("RegExp.prototype remains rooted during symbol bootstrap");
    object::define_own_symbol_property_partial(
        prototype,
        heap,
        match_sym,
        crate::object::PartialPropertyDescriptor {
            value: Some(match_root),
            writable: Some(true),
            enumerable: Some(false),
            configurable: Some(true),
            ..Default::default()
        },
    );
    object::define_own_symbol_property_partial(
        prototype,
        heap,
        search_sym,
        crate::object::PartialPropertyDescriptor {
            value: Some(search_root),
            writable: Some(true),
            enumerable: Some(false),
            configurable: Some(true),
            ..Default::default()
        },
    );
    object::define_own_symbol_property_partial(
        prototype,
        heap,
        replace_sym,
        crate::object::PartialPropertyDescriptor {
            value: Some(replace_root),
            writable: Some(true),
            enumerable: Some(false),
            configurable: Some(true),
            ..Default::default()
        },
    );
    object::define_own_symbol_property_partial(
        prototype,
        heap,
        split_sym,
        crate::object::PartialPropertyDescriptor {
            value: Some(split_root),
            writable: Some(true),
            enumerable: Some(false),
            configurable: Some(true),
            ..Default::default()
        },
    );
    object::define_own_symbol_property_partial(
        prototype,
        heap,
        match_all_sym,
        crate::object::PartialPropertyDescriptor {
            value: Some(match_all_root),
            writable: Some(true),
            enumerable: Some(false),
            configurable: Some(true),
            ..Default::default()
        },
    );
    Ok(())
}

