/// §22.2.6.10 `get RegExp.prototype.source`. When `this` is the
/// realm's `%RegExp.prototype%` (no `[[OriginalSource]]` slot)
/// returns the sentinel `"(?:)"`; non-RegExp non-prototype
/// receivers throw `TypeError`. Otherwise emits the spec's
/// `EscapeRegExpPattern(src, flags)`.
fn accessor_source(ctx: &mut NativeCtx<'_>, _args: &[Value]) -> Result<Value, NativeError> {
    let receiver = *ctx.this_value();

    let raw_units = if let Some(re) = receiver.as_regexp() {
        re.pattern_utf16(ctx.heap())
    } else if let Some(obj) = receiver.as_object() {
        let is_proto = ctx
            .interp_mut()
            .constructor_prototype_value("RegExp")
            .ok()
            .and_then(|p| p.as_object())
            .is_some_and(|p| p == obj);
        if !is_proto {
            return Err(NativeError::TypeError {
                name: "get RegExp.prototype.source",
                reason: "this is not a RegExp".to_string(),
            });
        }
        return Ok(Value::string(
            JsString::from_str("(?:)", ctx.heap_mut()).map_err(|_| oom("source"))?,
        ));
    } else {
        return Err(NativeError::TypeError {
            name: "get RegExp.prototype.source",
            reason: "this is not a RegExp".to_string(),
        });
    };
    let escaped = crate::regexp_prototype::escape_regexp_pattern_utf16(&raw_units);
    Ok(Value::string(
        JsString::from_utf16_units(&escaped, ctx.heap_mut()).map_err(|_| oom("source"))?,
    ))
}

/// §22.2.6.4 `get RegExp.prototype.flags`. Generic over any
/// receiver: reads each flag property via `[[Get]]`, applies
/// `ToBoolean`, and concatenates the flag letter when truthy.
/// Spec order is `d g i m s u v y`.
fn accessor_flags(ctx: &mut NativeCtx<'_>, _args: &[Value]) -> Result<Value, NativeError> {
    const NAME: &str = "get RegExp.prototype.flags";
    ctx.scope(|mut scope| {
        let receiver = scope.this();
        if !scope.raw(receiver).is_object_type() {
            return Err(NativeError::TypeError {
                name: NAME,
                reason: "this value must be an Object".to_string(),
            });
        }
        let mut out = String::with_capacity(8);
        for &(property, letter) in &[
            ("hasIndices", 'd'),
            ("global", 'g'),
            ("ignoreCase", 'i'),
            ("multiline", 'm'),
            ("dotAll", 's'),
            ("unicode", 'u'),
            ("unicodeSets", 'v'),
            ("sticky", 'y'),
        ] {
            let receiver_value = scope.raw(receiver);
            let value = crate::regexp_prototype::get_property_runtime(
                scope.context(),
                &receiver_value,
                property,
                NAME,
            )?;
            let value = scope.value(value);
            if scope.raw(value).to_boolean(scope.context().heap()) {
                out.push(letter);
            }
        }
        let flags = JsString::from_str(&out, scope.context().heap_mut()).map_err(|_| oom(NAME))?;
        let flags = scope.value(Value::string(flags));
        Ok(scope.finish(flags))
    })
}

