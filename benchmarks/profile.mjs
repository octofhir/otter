#!/usr/bin/env node
// Bottleneck finder: sample one benchmark on the otter-isolate thread and
// report SELF-time per symbol, hot leaf chains, and runtime-bridge candidates.
//
// The manual loop this replaces — build the profiling binary, hand-size the
// run so it lives long enough to attach, `sample` the isolate thread, then awk
// the indented call tree into self-time — was error-prone (mis-sized runs that
// exited before the sampler attached; inclusive counts mistaken for self time).
// This does all of it and highlights the shapes worth fixing.
//
// Usage:
//   node benchmarks/profile.mjs <bench>            # e.g. regex-heavy, prop-access
//   node benchmarks/profile.mjs <bench> --secs 10  # sample window (default 8)
//   node benchmarks/profile.mjs <bench> --top 30   # rows to print (default 25)
//   node benchmarks/profile.mjs <bench> --tier     # ALSO dump JIT tier/decline trace
//   node benchmarks/profile.mjs <bench> --no-build # skip the stale-binary rebuild
//
// A symbol's SELF time is what runs *in that frame*, not its callees — the
// number you optimize. `⚠` marks a runtime helper / bridge / boxing / re-
// materialization frame: self time there is usually a per-op cost that belongs
// inlined into compiled code or hoisted out of a loop.

import { spawn, spawnSync, execSync } from "node:child_process";
import { readFileSync, writeFileSync, existsSync, statSync, readdirSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve, extname } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, "..");
const SCRIPTS_DIR = join(HERE, "scripts");
const TMP_DIR = join(HERE, ".profile-tmp");
const BIN = join(ROOT, "target/profiling/otter");
const TARGET_SECS = 12; // wall time we grow the workload to, so the sampler has a stable window

// ---- args -----------------------------------------------------------------
const argv = process.argv.slice(2);
const flags = { secs: 8, top: 25, tier: false, build: true };
const positional = [];
for (let i = 0; i < argv.length; i++) {
  const a = argv[i];
  if (a === "--secs") flags.secs = Number(argv[++i]);
  else if (a === "--top") flags.top = Number(argv[++i]);
  else if (a === "--tier") flags.tier = true;
  else if (a === "--no-build") flags.build = false;
  else positional.push(a);
}
const ALL = positional[0] === "--all" || positional[0] === "all";
if (positional.length !== 1) {
  console.error(
    "usage: node benchmarks/profile.mjs <bench|--all> [--secs N] [--top N] [--tier] [--no-build]",
  );
  process.exit(2);
}

// ---- resolve the script ----------------------------------------------------
function resolveScript(name) {
  for (const cand of [name, `${name}.js`, `${name}.ts`]) {
    const p = join(SCRIPTS_DIR, cand);
    if (existsSync(p)) return p;
  }
  if (existsSync(name)) return name; // absolute / relative path
  console.error(`no such bench: ${name} (looked in ${SCRIPTS_DIR})`);
  process.exit(2);
}
// ---- build the profiling binary if stale -----------------------------------
function newestSourceMtime() {
  let newest = 0;
  const stack = [join(ROOT, "crates")];
  while (stack.length) {
    const dir = stack.pop();
    let entries;
    try {
      entries = readdirSync(dir, { withFileTypes: true });
    } catch {
      continue;
    }
    for (const e of entries) {
      if (e.name === "target" || e.name.startsWith(".")) continue;
      const p = join(dir, e.name);
      if (e.isDirectory()) stack.push(p);
      else if (e.name.endsWith(".rs")) {
        const m = statSync(p).mtimeMs;
        if (m > newest) newest = m;
      }
    }
  }
  return newest;
}
function ensureBinary() {
  const stale = !existsSync(BIN) || statSync(BIN).mtimeMs < newestSourceMtime();
  if (!stale) {
    console.log(`profiling binary up to date: ${BIN}`);
    return;
  }
  if (!flags.build) {
    console.error(`profiling binary is STALE and --no-build set — results will be wrong. Rebuild:\n  cargo build --profile profiling -p otter-cli`);
    process.exit(2);
  }
  console.log("building profiling binary (cargo build --profile profiling -p otter-cli)…");
  const r = spawnSync("cargo", ["build", "--profile", "profiling", "-p", "otter-cli"], {
    cwd: ROOT,
    stdio: "inherit",
  });
  if (r.status !== 0) process.exit(r.status ?? 1);
}

// ---- size the workload to ~TARGET_SECS -------------------------------------
// Wrap the whole script body in a function and call it M times. The bench
// scripts are pure JS/TS (no import/export), so function-scoping their top-level
// const/let/class is sound and gives a fresh run per call.
function writeWrapped(src, reps, scriptName) {
  if (!existsSync(TMP_DIR)) mkdirSync(TMP_DIR, { recursive: true });
  const wrapped =
    `// auto-generated by profile.mjs — ${scriptName} x${reps}\n` +
    `function __otterProfileBody() {\n${src}\n}\n` +
    `for (let __i = 0; __i < ${reps}; __i++) __otterProfileBody();\n`;
  const outPath = join(TMP_DIR, `${scriptName}.x${reps}${extname(scriptName)}`);
  writeFileSync(outPath, wrapped);
  return outPath;
}
function timeRun(runPath) {
  const t0 = Date.now();
  const r = spawnSync(BIN, ["run", runPath], {
    cwd: ROOT,
    env: { ...process.env, OTTER_JIT: "1" },
    stdio: "ignore",
  });
  if (r.status !== 0) {
    console.error(`bench exited non-zero (${r.status}) on ${runPath}; aborting`);
    process.exit(1);
  }
  return Date.now() - t0;
}
function buildSizedScript(scriptPath, scriptName) {
  const src = readFileSync(scriptPath, "utf8");
  if (/^\s*(import|export)\b/m.test(src)) {
    // Can't wrap a module body in a function; run as-is and hope it is long.
    return { path: scriptPath, reps: 1, wrapped: false };
  }
  // Two calibration runs isolate per-body cost from the (fixed, sizable)
  // profiling-binary startup — sizing off a single run undercounts because
  // startup dominates a short bench and the wrapped run then exits before the
  // sampler attaches.
  const lowReps = 1;
  const hiReps = 6;
  const tLow = timeRun(writeWrapped(src, lowReps, scriptName));
  const tHi = timeRun(writeWrapped(src, hiReps, scriptName));
  const perBody = Math.max(1, (tHi - tLow) / (hiReps - lowReps));
  let reps = Math.ceil((TARGET_SECS * 1000) / perBody);
  reps = Math.min(Math.max(reps, hiReps), 500_000);
  return { path: writeWrapped(src, reps, scriptName), reps, wrapped: true, perBody: Math.round(perBody) };
}

// ---- run + sample the otter-isolate thread ---------------------------------
function sampleRun(runPath, scriptName) {
  const child = spawn(BIN, ["run", runPath], {
    cwd: ROOT,
    env: { ...process.env, OTTER_JIT: "1" },
    stdio: "ignore",
  });
  // Give tier-up time to settle, then sample.
  const startDelayMs = 2000;
  const outFile = join(TMP_DIR, `${scriptName}.sample.txt`);
  const t = spawnSync(
    "bash",
    [
      "-c",
      `sleep ${startDelayMs / 1000}; sample ${child.pid} ${flags.secs} -file ${outFile} >/dev/null 2>&1 || true`,
    ],
    { cwd: ROOT },
  );
  try {
    process.kill(child.pid, "SIGKILL");
  } catch {}
  if (!existsSync(outFile)) {
    console.error(
      `sample produced no output — the bench likely exited before the ${startDelayMs / 1000}s attach delay. ` +
        `Try --secs smaller or a heavier bench.`,
    );
    process.exit(1);
  }
  return readFileSync(outFile, "utf8");
}

// ---- parse the isolate thread call tree into self-time ---------------------
function cleanSymbol(sym) {
  if (/^\?{2,}/.test(sym) || /unknown/i.test(sym)) return "‹JIT / compiled code›";
  return sym
    .replace(/::h[0-9a-f]{8,}/g, "")
    .replace(/\$u7b\$|\$u7d\$|\$LT\$|\$GT\$|\$u20\$|\$C\$/g, (m) =>
      ({ "$u7b$": "{", "$u7d$": "}", "$LT$": "<", "$GT$": ">", "$u20$": " ", "$C$": "," })[m],
    )
    .replace(/_\$.*?\$::/g, "::")
    .trim();
}

const BRIDGE_RE =
  /(stub|bridge|box_sloppy|_materialize|_reload|to_utf16_vec|invoke_native|_optimizing|prepare_.*call|draw_registers|run_load_global|jit_runtime|dispatch_bridge|from_utf16_units|set_match_result|alloc_.*with_roots)/i;

function parseIsolate(text, top) {
  const lines = text.split("\n");
  // Isolate thread section: from its header to the next "  NNN Thread_" header.
  let start = -1;
  const threadRe = /^\s*\d+\s+Thread_/;
  for (let i = 0; i < lines.length; i++) {
    if (/Thread_\S+:\s*otter-isolate/.test(lines[i])) {
      start = i;
      break;
    }
  }
  if (start < 0) {
    console.error("no otter-isolate thread in sample (single-threaded run? try another bench)");
    process.exit(1);
  }
  let end = lines.length;
  for (let i = start + 1; i < lines.length; i++) {
    if (threadRe.test(lines[i])) {
      end = i;
      break;
    }
  }
  const body = lines.slice(start, end);

  // Each frame line: <markers> <count> <symbol> (in <bin>) ...
  // depth = number of tree markers ( + ! : | ) before the count.
  const nodes = []; // {depth, count, sym, file}
  for (const line of body) {
    // `sample` encodes tree depth by INDENTATION COLUMN (spaces + connective
    // markers), not by marker count — a deep spine can carry a single `!` at
    // many levels. Depth = width of the leading prefix before the count.
    const m = line.match(/^([\s+!:|]*)(\d+)\s+(.*?)\s+\(in\s+/);
    if (!m) continue;
    const depth = m[1].length;
    const count = Number(m[2]);
    let sym = cleanSymbol(m[3]);
    const fileM = line.match(/\]\s+([\w./-]+:\d+)\s*$/);
    nodes.push({ depth, count, sym, file: fileM ? fileM[1] : "" });
  }
  if (!nodes.length) {
    console.error("could not parse any frames from the isolate thread");
    process.exit(1);
  }
  const total = nodes[0].count; // root frame = whole-thread sample count

  // self = count - sum(immediate children counts). Immediate child = next line
  // whose depth is exactly parentDepth+1, until a line with depth <= parentDepth.
  const self = new Array(nodes.length).fill(0);
  for (let i = 0; i < nodes.length; i++) self[i] = nodes[i].count;
  const stack = []; // indices, increasing depth
  for (let i = 0; i < nodes.length; i++) {
    while (stack.length && nodes[stack[stack.length - 1]].depth >= nodes[i].depth) stack.pop();
    if (stack.length) self[stack[stack.length - 1]] -= nodes[i].count;
    stack.push(i);
  }

  // Aggregate self by symbol.
  const bySym = new Map();
  for (let i = 0; i < nodes.length; i++) {
    if (self[i] <= 0) continue;
    const key = nodes[i].sym;
    const e = bySym.get(key) || { self: 0, file: nodes[i].file };
    e.self += self[i];
    if (!e.file) e.file = nodes[i].file;
    bySym.set(key, e);
  }
  const ranked = [...bySym.entries()]
    .map(([sym, e]) => ({ sym, self: e.self, file: e.file, pct: (100 * e.self) / total }))
    .sort((a, b) => b.self - a.self)
    .slice(0, top);

  // Hottest single leaf frames (highest self on one line) → the exact PCs.
  const leaves = nodes
    .map((n, i) => ({ ...n, selfCount: self[i] }))
    .filter((n) => n.selfCount > 0)
    .sort((a, b) => b.selfCount - a.selfCount)
    .slice(0, 8);

  return { total, ranked, leaves };
}

// ---- optional tier / decline trace -----------------------------------------
function tierTrace(scriptPath) {
  const r = spawnSync(join(ROOT, "target/release/otter") /* may not exist */, ["--version"], {
    stdio: "ignore",
  });
  const bin = r.status === 0 ? join(ROOT, "target/release/otter") : BIN;
  const out = spawnSync(bin, ["run", scriptPath], {
    cwd: ROOT,
    env: { ...process.env, OTTER_JIT: "1", OTTER_JIT_TRACE: "summary" },
    encoding: "utf8",
  });
  const text = (out.stderr || "") + (out.stdout || "");
  const lines = text
    .split("\n")
    .filter((l) => /optimizing tier|osr bail|decline|OptimizedCode|CraneliftCode|inlined|bridged|×/.test(l));
  return lines.slice(0, 40);
}

// ---- profile one bench -----------------------------------------------------
function profileOne(scriptPath, scriptName) {
  const sized = buildSizedScript(scriptPath, scriptName);
  const sampleText = sampleRun(sized.path, scriptName);
  const { total, ranked, leaves } = parseIsolate(sampleText, flags.top);
  const compiled = ranked.find((r) => r.sym === "‹JIT / compiled code›");
  const bridges = ranked.filter((r) => BRIDGE_RE.test(r.sym) && r.sym !== "‹JIT / compiled code›");
  const topVm = ranked.find((r) => r.sym !== "‹JIT / compiled code›");
  return { sized, total, ranked, leaves, compiled, bridges, topVm };
}

// ---- single-bench report ---------------------------------------------------
function reportOne(scriptName, res) {
  const { sized, total, ranked, leaves, compiled, bridges } = res;
  console.log(
    `profiling ${scriptName}` +
      (sized.wrapped ? ` (~${sized.perBody}ms/run → x${sized.reps} ≈ ${TARGET_SECS}s)` : " (as-is)") +
      `, sampling ${flags.secs}s on otter-isolate…`,
  );
  if (compiled) {
    console.log(
      `\nNOTE: ${compiled.pct.toFixed(1)}% of self time is in compiled JIT code (not symbolicated). ` +
        `A high value means this bench is codegen-quality-bound, not runtime-bound — ` +
        `the remaining VM symbols below are the non-compiled residue.`,
    );
  }
  console.log(`\n=== SELF time by symbol (otter-isolate, ${total} samples) ===`);
  console.log("  self   %    symbol");
  for (const r of ranked) {
    const flag = BRIDGE_RE.test(r.sym) && r.sym !== "‹JIT / compiled code›" ? " ⚠" : "";
    const pct = r.pct.toFixed(1).padStart(4);
    console.log(`${String(r.self).padStart(6)}  ${pct}  ${r.sym}${flag}${r.file ? `  [${r.file}]` : ""}`);
  }
  if (bridges.length) {
    const bpct = bridges.reduce((s, r) => s + r.pct, 0).toFixed(1);
    console.log(
      `\n⚠ runtime-helper / bridge self-time: ${bpct}% across ${bridges.length} symbol(s). ` +
        `These are per-op costs — candidates to inline into compiled code, hoist out of the loop, or cache.`,
    );
  }
  console.log(`\n=== hottest single frames (exact PCs) ===`);
  for (const l of leaves) {
    console.log(`${String(l.selfCount).padStart(6)}  ${l.sym}${l.file ? `  [${l.file}]` : ""}`);
  }
}

// ---- all-benches landscape -------------------------------------------------
function runAll() {
  const benches = readdirSync(SCRIPTS_DIR)
    .filter((f) => /\.(js|ts)$/.test(f))
    .sort();
  const rows = [];
  for (const f of benches) {
    const p = join(SCRIPTS_DIR, f);
    try {
      const res = profileOne(p, f);
      rows.push({
        bench: f,
        compiled: res.compiled ? res.compiled.pct : 0,
        topVm: res.topVm ? res.topVm.sym : "-",
        topVmPct: res.topVm ? res.topVm.pct : 0,
        bridgePct: res.bridges.reduce((s, r) => s + r.pct, 0),
        topBridge: res.bridges[0] ? res.bridges[0].sym : "",
      });
      process.stderr.write(`  ✓ ${f}\n`);
    } catch (e) {
      process.stderr.write(`  ✗ ${f}: ${e.message}\n`);
    }
  }
  // Sort by where the biggest single, actionable non-compiled cost sits.
  rows.sort((a, b) => Math.max(b.bridgePct, b.topVmPct) - Math.max(a.bridgePct, a.topVmPct));
  const short = (s) => (s.length > 46 ? s.slice(0, 45) + "…" : s).padEnd(46);
  console.log(`\n=== bottleneck landscape (self time, all benches) ===`);
  console.log(`${"bench".padEnd(24)} cmpl%  top-VM-symbol${" ".repeat(35)}vm%   ⚠brdg%  top-bridge`);
  for (const r of rows) {
    console.log(
      `${r.bench.padEnd(24)} ${r.compiled.toFixed(0).padStart(4)}  ${short(r.topVm)} ` +
        `${r.topVmPct.toFixed(1).padStart(5)}  ${r.bridgePct.toFixed(1).padStart(6)}  ${r.topBridge}`,
    );
  }
  console.log(
    `\ncmpl% = time in compiled JIT code (codegen-bound); ⚠brdg% = runtime-helper/bridge self-time ` +
      `(inline/hoist/cache candidates). Profile one bench for detail: node benchmarks/profile.mjs <bench>`,
  );
}

// ---- main ------------------------------------------------------------------
ensureBinary();
if (ALL) {
  runAll();
} else {
  const scriptPath = resolveScript(positional[0]);
  const scriptName = scriptPath.split("/").pop();
  const res = profileOne(scriptPath, scriptName);
  reportOne(scriptName, res);
  if (flags.tier) {
    console.log(`\n=== JIT tier / decline trace (OTTER_JIT_TRACE=summary) ===`);
    for (const l of tierTrace(scriptPath)) console.log(l);
  }
}
